print("=*Velocidade média*=")

distance = float(input("A distância entre a cidade A e B é de (informe a distância em KM): "))
time = float(input("Qual o tempo de viagem entre a cidade A e B? (informe o tempo em minutos)"))

Vm = distance / time

print("O carro viajou entra cidade A e B em uma velocidade média de",Vm,"km/h")
